def merge_sort(arr):
  if len(arr) > 1:
    left_index = 0
    right_index = len(arr)
    mid = (left_index + right_index) // 2

    left = arr[:mid]
    right = arr[mid:right_index]

    merge_sort(left)
    merge_sort(right)

    i = 0
    j = 0
    k = 0

    while(i < len(left) and j < len(right)):
      if(left[i] < right[j]):
        arr[k] = left[i]
        i+=1
      else:
        arr[k] = right[j]
        j+=1
      k+=1

    while(i < len(left)):
      arr[k] = left[i]
      i+=1
      k+=1

    while(j < len(right)):
      arr[k] = right[j]
      j+=1
      k+=1
    
  return arr

