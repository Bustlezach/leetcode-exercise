
function mergeSort(arr) {
  if (arr.length > 1){
    const rightIndex = arr.length;
    const mid = Math.floor((0 + rightIndex) / 2);

    const left = arr.slice(0, mid);
    const right = arr.slice(mid, rightIndex);

    mergeSort(left);
    mergeSort(right);

    let i = 0;
    let j = 0;
    let k = 0;
    while (i < left.length && j < right.length) {
      if(left[i] <= right[j]) {
        arr[k++] = left[i++];
      } else {
        arr[k++] = right[j++];
      }
    }
    while(i < left.length) {
      arr[k++] = left[i++];
    }
    while(j < right.length) {
      arr[k++] = right[j++];
    }
  }
  return arr;
}

